def Make_T_with_TPS(frames, img, Tb_list, mid=0, max_ransac_iter=5):

  import numpy.linalg as LA
  from scipy.spatial.distance import cdist
  from scipy.linalg import solve

  def tps_kernel(r):
    r[r == 0] = 1e-20
    return r**2 * np.log(r)

  def compute_tps_weights(src_pts, dst_pts, reg=1e-3):
    n = src_pts.shape[0]
    K = tps_kernel(cdist(src_pts, src_pts))
    P = np.hstack((np.ones((n, 1)), src_pts))
    L = np.vstack([
        np.hstack([K + reg * np.eye(n), P]),
        np.hstack([P.T, np.zeros((3, 3))])
    ])
    V = np.vstack([dst_pts, np.zeros((3, 2))])
    W = solve(L, V)
    return W[:n], W[n:]

  def compute_tps_transform(src_pts, dst_pts):
    return compute_tps_weights(src_pts, dst_pts)

  H_list = []
  TPS_list = []

  sift = cv2.SIFT_create()
  bf = cv2.BFMatcher()

  kp0, des0 = sift.detectAndCompute(img, None)

  for i in range(len(frames)):
    kp, des = sift.detectAndCompute(frames[i], None)
    matches = bf.knnMatch(des0, des, k=2)

    good_matches = []
    for m, n in matches:
        if m.distance < 0.75 * n.distance:
            good_matches.append(m)

    pts0 = np.float32([kp0[m.queryIdx].pt for m in good_matches])
    pts1 = np.float32([kp[m.trainIdx].pt for m in good_matches])

    # --- マルチホモグラフィRANSAC ---
    used = np.zeros(len(pts0), dtype=bool)
    clusters_src = []
    clusters_dst = []

    for _ in range(max_ransac_iter):
        if np.sum(~used) < 4:
            break
        sel_src = pts1[~used]
        sel_dst = pts0[~used]
        H, mask = cv2.findHomography(sel_src, sel_dst, cv2.RANSAC, 4.0)
        if H is None or np.sum(mask) < 4:
            break
        inliers = np.where(~used)[0][mask.ravel() == 1]
        used[inliers] = True
        clusters_src.append(pts1[inliers])
        clusters_dst.append(pts0[inliers])

    # --- TPS変換を計算 ---
    all_src = np.vstack(clusters_src)
    all_dst = np.vstack(clusters_dst)
    if len(all_src) >= 6:
        w_tps, a_tps = compute_tps_transform(all_src, all_dst)
        TPS_list.append((w_tps, a_tps, all_src))  # TPS変換関数の格納
        H_list.append(None)  # TPSを使うのでHomographyは不要
    else:
        # fallback: homography
        H, _ = cv2.findHomography(pts1, pts0, cv2.RANSAC, 4.0)
        H_list.append(H)
        TPS_list.append(None)

  return H_list, TPS_list

