  Hr = np.zeros_like(Gb).astype(float)
  Hb = np.zeros_like(Gb).astype(float)
  Hf = np.zeros_like(Gb).astype(float)
  Wb = np.ones_like(Gb).astype(float)
  m = 3
  # ratio = a
  thr = 0.05 * 255
  a = 1.2
  Gf = GF.astype(float)
  Gr = GR.astype(float)
  Gb = gray_mat(np.clip(Gf.astype(int) - Gr.astype(int),MIN_PIXEL,MAX_PIXEL))
  Hf = gray_mat(np.where(Gf >thr, MAX_PIXEL, MIN_PIXEL))
  Hb = gray_mat(np.where(Gb > thr, MAX_PIXEL, MIN_PIXEL))
  Hr = gray_mat(np.where(Gr > thr, MAX_PIXEL, MIN_PIXEL))



  for i in range(int(frame_height) * int(frame_width)):

    for j in range(frame_num):
      for k in range(color_num):
        if(Hf[j,i,k] == MAX_PIXEL and abs(Gb[j,i,k]) > a * abs(Gr[j,i,k])):
          Wb[j,i,k] = 0

        elif(Hf[j,i,k] == MAX_PIXEL and abs(Gr[j,i,k]) > a * abs(Gb[j,i,k])):
          Wb[j,i,k] = 5

        elif(Hf[j,i,k] == MIN_PIXEL and (abs(Gb[j,i,k]) > thr or abs(Gr[j,i,k]) > thr)):
          Wb[j,i,k] = 5

        else:
          Wb[j,i,k] = 1

    for k in range(color_num):
      if(sum(Wb[:,i,k]) < m):
        Wb[:,i,k] = 0


  Wb = gray_mat(np.clip(Wb,0,255).astype(np.uint8))
  return Wb

