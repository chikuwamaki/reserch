def NCC(truth, est):
    ncc_ave = []
    for i in range(frame_num):
      #temp= cv2.matchTemplate(est[i], truth[i], cv2.TM_CCOEFF_NORMED)
      #ncc_ave.append(temp)
        for j in range(color_num):
            img1 = truth[i][:,:,j].flatten().astype(np.float32)
            img2 = est[i][:,:,j].flatten().astype(np.float32)

            img1 -= np.mean(img1)
            img2 -= np.mean(img2)

            denom = LA.norm(img1) * LA.norm(img2)
            if denom != 0:
                ncc_ave.append(np.sum(img1 * img2) / denom)
            else:
                ncc_ave.append(0.0)

    return np.mean(ncc_ave)

