def Make_edge_D2(B):
  DB = np.zeros_like(B).astype(int)
  size = B.shape[1]
  w = int(frame_width)

  for i in range(B.shape[0]):
    for j in range(B.shape[1]):
      for k in range(color_num):
        DB[i,j,k] = int(B[i,(j+1)%size,k]) - int(B[i,(j-1)%size,k]) + int(B[i,(j+1-w)%size,k]) + int(B[i,(j+1+w)%size,k]) - int(B[i,(j-1-w)%size,k]) - int(B[i,(j-1+w)%size,k])
        DB[i,j,k] += int(B[i,(j+w)%size,k]) - int(B[i,(j-w)%size,k]) - int(B[i,(j+1-w)%size,k]) + int(B[i,(j+1+w)%size,k]) - int(B[i,(j-1-w)%size,k]) + int(B[i,(j-1+w)%size,k])

  DB = np.clip(np.abs(DB),MIN_PIXEL,MAX_PIXEL).astype(np.uint8)
  return DB

