def Make_edge_2d(B):
  DB = np.zeros_like(B).astype(int)
  size = B.shape[1]
  for i in range(B.shape[0]):
    for j in range(B.shape[1]):
      DB[i,j] = 2 * int(B[i,j%size]) - int(B[i,(j+1)%size]) - int(B[i,(j+int(frame_width))%size])

  DB = np.clip(np.abs(DB),MIN_PIXEL,MAX_PIXEL).astype(np.uint8)
