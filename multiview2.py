def optimize(F,thr = 10,max_iter = 300):

  def shrinkage_operator(x, tau):
    return np.sign(x) * np.maximum((np.abs(x) - tau), np.zeros_like(x))

  F = np.clip(F, 0,255).astype(np.float32)
  Y1 = np.zeros_like(F)
  Y2 = np.zeros_like(F)
  Y3 = np.zeros_like(F)
  mu = 0.05
  #mu = F.shape[1] * F.shape[2] / (4 * LA.norm(F.reshape(-1, 3), ord=1))
  p = 1.2
  Invmu = 1/mu

  B = np.zeros_like(F)
  A = np.zeros_like(F)
  E = np.zeros_like(F)
  C = np.zeros_like(F)
  N = np.zeros_like(F)

  tol = 1e-7 * LA.norm(F)
  error = np.inf
  count = 0

  #while (LA.norm(B - A) + LA.norm(F - B - E - N))>thr and count < max_iter:
  while count < max_iter:
    print(f"count:{count}")

    U1, S1, Vh1 = LA.svd(B[:,:,0] + Y2[:,:,0] * Invmu, full_matrices=False)
    U2, S2, Vh2 = LA.svd(B[:,:,1] + Y2[:,:,1] * Invmu, full_matrices=False)
    U3, S3, Vh3 = LA.svd(B[:,:,2] + Y2[:,:,2] * Invmu, full_matrices=False)

    U = np.zeros((U1.shape[0], U1.shape[1], 3))
    S = np.zeros((S1.shape[0], 3))
    Vh = np.zeros((Vh1.shape[0], Vh1.shape[1], 3))
    S_lowrank = np.zeros_like(S)

    U[:,:,0] = U1
    U[:,:,1] = U2
    U[:,:,2] = U3

    S[:,0] = S1
    S[:,1] = S2
    S[:,2] = S3

    Vh[:,:,0] = Vh1
    Vh[:,:,1] = Vh2
    Vh[:,:,2] = Vh3

    S_lowrank[0,0] = S[0,0]
    S_lowrank[0,1] = S[0,1]
    S_lowrank[0,2] = S[0,2]

    U = U.astype(np.float32)
    S = S.astype(np.float32)
    Vh = Vh.astype(np.float32)
    S_lowrank = S_lowrank.astype(np.float32)

    for i in range(color_num):

      A[:,:,i] = U[:,:,i] @ np.diag(S_lowrank[:,i]) @ Vh[:,:,i]
      E[:,:,i] = shrinkage_operator(F[:,:,i] - B[:,:,i] - N[:,:,i] + Y1[:,:,i] * Invmu ,lam1 * Invmu)
      #C[:,:,i] = shrinkage_operator()
      N[:,:,i] = (mu / (lam2 + mu)) * (F[:,:,i] - B[:,:,i] - E[:,:,i] + Y1[:,:,i] * Invmu)

      #Inv(transpose(D)*D + 2I) * T
      T = F[:,:,i] - E[:,:,i] - N[:,:,i] + Invmu * Y1[:,:,i] + A[:,:,i] -Invmu * Y2[:,:,i]
      #T = F - E N + Invmu * Y1 + A -Invmu * Y2 + transpose(D) @ (C - Invmu * Y3)
      B[:,:,i] = (1/2)*T

      Y1[:,:,i] = Y1[:,:,i] + mu * (F[:,:,i] - B[:,:,i] - E[:,:,i] - N[:,:,i])
      Y2[:,:,i] = Y2[:,:,i] + mu * (B[:,:,i] - A[:,:,i])
      #Y3[:,:,i] = Y3[:,:,i] + mu * (D * B - C)

    error = LA.norm(B - A) + LA.norm(F - B - E - N)
    count = count + 1

    if error <= tol:
        print("converted! error:{}".format(error))
        break
    else:
      print("error:{}".format(error))

    mu = p * mu
    Invmu = 1/mu

  B = np.clip(B, 0,255).astype(np.uint8)
  return B




def img_to_mat(frames):
  Fdash = np.zeros((int(frame_num) , int(frame_height) * int(frame_width), 3), dtype=np.uint8)

  for i in range(frame_num):
    for j in range(color_num):
      Fdash[i,:,j] = frames[i,:,:,j].flatten()

  return Fdash

def mat_to_img(F):
  frames = np.zeros((int(frame_num), int(frame_height), int(frame_width), 3), dtype=np.uint8)

  for i in range(frame_num):
    for j in range(color_num):
      frames[i,:,:,j] = F[i,:,j].reshape(int(frame_height),int(frame_width))

  return frames

