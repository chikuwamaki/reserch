def TP(Fdash,H_list):
  frames = mat_to_img(Fdash)
  warped_img = np.zeros_like(frames)

  h, w = frames.shape[1:3]

  for i in range(frame_num):
    warped_img[i] = cv2.warpPerspective(frames[i], H_list[i], (w, h))

  F = img_to_mat(warped_img)
  return F

